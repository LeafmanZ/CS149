public class BasicMath {
    
    /**
    * Takes inputs and does a math operation.
    *
    * @param x we have a number
    * @param y we have a number
    * @return add product
    */  
    public static double add(double x, double y) {
        double sum;
        sum = x + y;
        return sum;
    }
    
    /**
    * Takes inputs and does a math operation.
    *
    * @param x we have a number
    * @param y we have a number
    * @return subtract product
    */  
    public static double subtract(double x, double y) {
        double diff;
        diff = x - y;
        return diff;
    }
    
    /**
    * Takes inputs and does a math operation.
    *
    * @param x we have a number
    * @param y we have a number
    * @return multiply product
    */  
    public static double multiply(double x, double y) {
        double product;
        product = x * y;
        return product;
    }
    
    /**
    * Takes inputs and does a math operation.
    *
    * @param x we have a number
    * @param y we have a number
    * @return divide product
    */  
    public static double divide(double x, double y) {
        double quotient;
        quotient = x / y;
        return quotient;
    }
    
}

